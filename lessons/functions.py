"""Practice with functions"""


# Define a function
def sum(num1: int, num2: int) -> int:
    """Return num1 + num2."""
    return num1 + num2


# parameters: (num1: int, num2: int)


# Call the function
print(sum(num1=11, num2=3))  # <- 1 and 10 are arguments
